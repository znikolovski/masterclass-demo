#!/usr/bin/env node
/**
 * Create or update WKND Analytics segments on ags050wknd via Analytics 2.0 API.
 *
 * Prerequisites:
 *   - Adobe Developer Console project with Analytics 2.0 API + OAuth S2S credentials
 *   - Report suite ags050wknd enabled for Experience Cloud audience publishing
 *   - Segment Publishing permission on your Admin Console product profile
 *
 * Environment variables:
 *   AA_CLIENT_ID          Adobe OAuth client ID (same as x-api-key)
 *   AA_CLIENT_SECRET      Adobe OAuth client secret
 *   AA_GLOBAL_COMPANY_ID  Global company ID — default resolved via discovery/me (WKND: adobeae7)
 *   AA_LOGIN_COMPANY      Preferred login company when auto-resolving (default: adobeae7)
 *   AA_REPORT_SUITE_ID    Default: ags050wknd
 *   AA_IMS_URL            Default: https://ims-na1.adobelogin.com/ims/token/v3
 *
 * Usage:
 *   node tools/scripts/publish-wknd-segments.mjs --dry-run
 *   node tools/scripts/publish-wknd-segments.mjs --apply
 *   node tools/scripts/publish-wknd-segments.mjs --apply --publish-ec-note
 *
 * Experience Cloud: the Analytics API does not enable "Publish to Experience Cloud".
 * After --apply, enable publishing per segment in Analytics UI (Components → Segments)
 * or ask an admin with Segment Publishing rights.
 *
 * Alternative: Adobe Analytics MCP upsertSegment with segmentBody from
 * tools/scripts/output/wknd-segments/*.json (generated by --dry-run --export).
 */

import { mkdirSync, writeFileSync } from 'node:fs';
import { dirname, join } from 'node:path';
import { fileURLToPath } from 'node:url';
import { RSID, WKND_SEGMENTS } from './lib/wknd-analytics-segments.mjs';

const __dirname = dirname(fileURLToPath(import.meta.url));
const OUTPUT_DIR = join(__dirname, 'output', 'wknd-segments');

const SCOPES = [
  'openid',
  'AdobeID',
  'read_organizations',
  'additional_info.projectedProductContext',
  'additional_info.roles',
  'adobeio_api',
  'read_client',
].join(',');

/** @param {string[]} argv */
function parseArgs(argv) {
  const opts = {
    dryRun: true,
    apply: false,
    exportJson: false,
    listExisting: false,
    limit: null,
  };
  argv.forEach((arg) => {
    if (arg === '--dry-run') opts.dryRun = true;
    else if (arg === '--apply') {
      opts.apply = true;
      opts.dryRun = false;
    } else if (arg === '--export') opts.exportJson = true;
    else if (arg === '--list') opts.listExisting = true;
    else if (arg.startsWith('--limit=')) {
      opts.limit = Math.max(1, parseInt(arg.slice(8), 10) || WKND_SEGMENTS.length);
    }
  });
  return opts;
}

function requireEnv(name) {
  const value = process.env[name];
  if (!value) throw new Error(`Missing env ${name}`);
  return value;
}

async function getAccessToken() {
  const clientId = requireEnv('AA_CLIENT_ID');
  const clientSecret = requireEnv('AA_CLIENT_SECRET');
  const imsUrl = process.env.AA_IMS_URL || 'https://ims-na1.adobelogin.com/ims/token/v3';
  const body = new URLSearchParams({
    client_id: clientId,
    client_secret: clientSecret,
    grant_type: 'client_credentials',
    scope: SCOPES,
  });
  const resp = await fetch(imsUrl, {
    method: 'POST',
    headers: { 'Content-Type': 'application/x-www-form-urlencoded' },
    body,
  });
  if (!resp.ok) {
    throw new Error(`IMS token failed (${resp.status}): ${await resp.text()}`);
  }
  const data = await resp.json();
  if (!data.access_token) throw new Error('IMS response missing access_token');
  return { token: data.access_token, clientId };
}

/**
 * Resolve global company ID from env or Analytics Discovery API.
 * @param {string} token
 * @param {string} clientId
 */
async function resolveGlobalCompanyId(token, clientId) {
  if (process.env.AA_GLOBAL_COMPANY_ID?.trim()) {
    return process.env.AA_GLOBAL_COMPANY_ID.trim();
  }

  const preferred = (process.env.AA_LOGIN_COMPANY || 'adobeae7').trim();
  const resp = await fetch('https://analytics.adobe.io/discovery/me', {
    headers: {
      Authorization: `Bearer ${token}`,
      'x-api-key': clientId,
      Accept: 'application/json',
    },
  });
  if (!resp.ok) {
    throw new Error(
      `Missing env AA_GLOBAL_COMPANY_ID and discovery/me failed (${resp.status}). `
      + 'Set AA_GLOBAL_COMPANY_ID=adobeae7 for WKND (login company Adobe AGS050).',
    );
  }
  const data = await resp.json();
  const companies = (data.imsOrgs || []).flatMap((org) => org.companies || []);
  if (!companies.length) {
    throw new Error('discovery/me returned no companies — set AA_GLOBAL_COMPANY_ID manually.');
  }

  const match = companies.find((c) => c.globalCompanyId === preferred)
    || companies.find((c) => c.companyName?.toLowerCase().includes('ags050'))
    || companies[0];

  console.log(`  Resolved AA_GLOBAL_COMPANY_ID=${match.globalCompanyId} (${match.companyName})`);
  return match.globalCompanyId;
}

/**
 * @param {string} token
 * @param {string} clientId
 * @param {string} companyId
 */
function apiHeaders(token, clientId, companyId) {
  return {
    Authorization: `Bearer ${token}`,
    'x-api-key': clientId,
    'x-proxy-global-company-id': companyId,
    'Content-Type': 'application/json',
    Accept: 'application/json',
  };
}

/**
 * @param {string} token
 * @param {string} clientId
 * @param {string} companyId
 * @param {string} rsid
 */
async function listSegments(token, clientId, companyId, rsid) {
  const params = new URLSearchParams({
    rsid,
    limit: '1000',
    includeType: 'all',
    expansion: 'definition,publishingStatus',
  });
  const url = `https://analytics.adobe.io/api/${companyId}/segments?${params}`;
  const resp = await fetch(url, { headers: apiHeaders(token, clientId, companyId) });
  if (!resp.ok) throw new Error(`List segments failed (${resp.status}): ${await resp.text()}`);
  const data = await resp.json();
  return data.content || [];
}

/**
 * @param {string} token
 * @param {string} clientId
 * @param {string} companyId
 * @param {object} segmentBody
 * @param {string} [segmentId]
 */
async function upsertSegment(token, clientId, companyId, segmentBody, segmentId) {
  const url = segmentId
    ? `https://analytics.adobe.io/api/${companyId}/segments/${segmentId}?expansion=definition,publishingStatus`
    : `https://analytics.adobe.io/api/${companyId}/segments/?expansion=definition,publishingStatus`;
  const resp = await fetch(url, {
    method: 'PUT',
    headers: apiHeaders(token, clientId, companyId),
    body: JSON.stringify(segmentBody),
  });
  const text = await resp.text();
  if (!resp.ok) {
    throw new Error(`Upsert "${segmentBody.name}" failed (${resp.status}): ${text}`);
  }
  return JSON.parse(text);
}

function exportSegments(segments) {
  mkdirSync(OUTPUT_DIR, { recursive: true });
  const manifest = [];
  segments.forEach((segment) => {
    const slug = segment.name.toLowerCase().replace(/[^a-z0-9]+/g, '-').replace(/(^-|-$)/g, '');
    const path = join(OUTPUT_DIR, `${slug}.json`);
    writeFileSync(path, JSON.stringify(segment, null, 2));
    manifest.push({ name: segment.name, file: path });
  });
  writeFileSync(join(OUTPUT_DIR, 'manifest.json'), JSON.stringify(manifest, null, 2));
  return manifest;
}

function printExperienceCloudSteps(segmentNames) {
  console.log('\nExperience Cloud publishing (manual — API cannot toggle this):');
  console.log('  1. Analytics → Admin → Report suite ags050wknd → Experience Cloud Audiences → Enabled');
  console.log('  2. Analytics → Components → Segments');
  console.log('  3. For each segment, Edit → enable "Publish to Experience Cloud"');
  console.log('  4. Audiences appear in Experience Cloud Audience Library within ~4 hours');
  console.log(`  5. Segments to publish (${segmentNames.length}):`);
  segmentNames.forEach((name) => console.log(`     - ${name}`));
  console.log('\nOr use Adobe Analytics MCP upsertSegment after Omni.Tools.MCPAccess is granted');
  console.log('on company adobeae7 / report suite ags050wknd.');
}

async function main() {
  const opts = parseArgs(process.argv.slice(2));
  const segments = opts.limit ? WKND_SEGMENTS.slice(0, opts.limit) : WKND_SEGMENTS;

  console.log(`WKND segment publisher (${segments.length} definitions, rsid=${RSID})`);

  if (opts.exportJson || opts.dryRun) {
    const manifest = exportSegments(segments);
    console.log(`  Exported: ${OUTPUT_DIR} (${manifest.length} files)`);
  }

  if (opts.dryRun) {
    console.log('\nDry run — no API calls.');
    console.log('Segments:');
    segments.forEach((s) => console.log(`  - ${s.name}`));
    printExperienceCloudSteps(segments.map((s) => s.name));
    return;
  }

  const { token, clientId } = await getAccessToken();
  const companyId = await resolveGlobalCompanyId(token, clientId);
  const rsid = process.env.AA_REPORT_SUITE_ID || RSID;

  if (opts.listExisting) {
    const existing = await listSegments(token, clientId, companyId, rsid);
    existing
      .filter((s) => s.name?.startsWith('WKND') || s.name?.startsWith('EDS'))
      .forEach((s) => console.log(`  ${s.id}  ${s.name}  published=${s.publishingStatus || '—'}`));
    return;
  }

  const existing = await listSegments(token, clientId, companyId, rsid);
  const byName = new Map(existing.map((s) => [s.name, s]));

  const results = [];
  for (const segment of segments) {
    const match = byName.get(segment.name);
    const saved = await upsertSegment(
      token,
      clientId,
      companyId,
      { ...segment, rsid },
      match?.id,
    );
    results.push({
      name: saved.name,
      id: saved.id,
      publishingStatus: saved.publishingStatus || null,
      action: match ? 'updated' : 'created',
    });
    console.log(`  ✓ ${saved.name} (${match ? 'updated' : 'created'}) ${saved.id}`);
  }

  const summaryPath = join(OUTPUT_DIR, `publish-run-${Date.now()}.json`);
  mkdirSync(OUTPUT_DIR, { recursive: true });
  writeFileSync(summaryPath, JSON.stringify({ rsid, companyId, results }, null, 2));
  console.log(`\nSummary: ${summaryPath}`);
  printExperienceCloudSteps(results.map((r) => r.name));
}

main().catch((err) => {
  console.error(err.message || err);
  process.exit(1);
});
